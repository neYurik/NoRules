package me.nrules.util;

import net.minecraft.util.math.Vec3d;

public class Vec3DUtil {

    public static double getX(Vec3d vec) {
        return vec.x;
    }

    public static double getY(Vec3d vec) {
        return vec.y;
    }

    public static double getZ(Vec3d vec) {
        return vec.z;
    }


}
